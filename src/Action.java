public class Action 
{
	private int x, y;
	public Action()
	{
		
	}
	public Action(int x, int y)
	{
		this.x = x;
		this.y = y;
	}
	
	public int getX()
	{
		return x;
	}
	public int getY()
	{
		return y;
	}
	public void setAction(int x, int y)
	{
		this.x = x;
		this.y = y;
	}
}
